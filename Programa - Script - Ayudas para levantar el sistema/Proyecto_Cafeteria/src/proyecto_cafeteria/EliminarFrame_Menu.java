    private boolean isNullorEmpty(String text){
        return text == null || text.isEmpty();
    }
    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        db_menu = new DB_Menu();
        boolean campoLleno = !isNullorEmpty(txtNombre.getText());
        boolean campoValidacion = isNullorEmpty(ValidacionDatos.texto(txtNombre.getText()));
        
        if(campoLleno){
            if(campoValidacion){
                db_menu.buscarMenu(txtNombre.getText());
            }else{
                JOptionPane.showMessageDialog(null, "SE DETECTO CARACTERES NO PERMITIDOS EN EL CAMPO");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingresa un dato en el campo mostrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        db_menu = new DB_Menu();
        boolean campoLleno = !isNullorEmpty(txtNombre.getText());
        boolean campoValidacion = isNullorEmpty(ValidacionDatos.texto(txtNombre.getText()));
        
         if(campoLleno){
            if(campoValidacion){
                db_menu.eliminarMenu(txtNombre.getText());
                txtNombre.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "SE DETECTO CARACTERES NO PERMITIDOS EN EL CAMPO");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingresa un dato en el campo mostrado");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


