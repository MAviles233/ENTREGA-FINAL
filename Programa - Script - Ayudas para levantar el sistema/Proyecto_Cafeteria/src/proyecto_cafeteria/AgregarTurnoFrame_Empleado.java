    private boolean isNullorEmpty(String text){
      return text == null || text.isEmpty();
    }
    
    private void limpiarCampos(){
        txtNombreEmpleado.setText("");
        txtAnio.setText("");
        txtMes.setText("");
        cmbTurno.setSelectedIndex(-1);
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        db_turno = new DB_Turno();
        turno = new Turno();
        
        boolean camposLlenos = !isNullorEmpty(txtAnio.getText()) && !isNullorEmpty(txtMes.getText()) && !isNullorEmpty(txtNombreEmpleado.getText())
                && cmbTurno.getSelectedIndex()>-1;
        boolean camposValidos = isNullorEmpty(ValidacionDatos.texto(txtNombreEmpleado.getText()));
        
        if(camposLlenos){
           if(camposValidos){
               turno.setEmpleado(txtNombreEmpleado.getText());
               turno.setTipoTurno(cmbTurno.getSelectedItem().toString());
               turno.setMes(Integer.parseInt(txtMes.getText()));
               turno.setAnio(Integer.parseInt(txtAnio.getText()));
               
               db_turno.guardarTurno(turno);
               limpiarCampos();
           }else{
               JOptionPane.showInternalMessageDialog(null, "SE DETECTARON CAMPOS CON INFORMACION NO VALIDA");
           }
        }else{
            JOptionPane.showInternalMessageDialog(null, "FALTAN DE LLENAR CAMPOS");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


